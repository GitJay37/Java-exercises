
public class OperadoresRelacionales {

	public static void main(String[] args) {
		// Related operators:
		
		int varA = 200, varB = 500;
		boolean result;
		
		result = varA > varB;
		System.out.println(result);
		
		result = varA < varB;
		System.out.println(result);

		result = varA >= varB;
		System.out.println(result);

		result = varA <= varB;
		System.out.println(result);
		
		result = varA == varB;
		System.out.println(result);

		result = varA != varB;
		System.out.println(result);

	}

}
